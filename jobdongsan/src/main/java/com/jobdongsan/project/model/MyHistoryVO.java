package com.jobdongsan.project.model;

public class MyHistoryVO {
	private int myHistoryNo;
	private int resultNo;
	private int videoNo;
	private int mapJobNo;
	private String memId;
	
	
	public int getMyHistoryNo() {
		return myHistoryNo;
	}
	public void setMyHistoryNo(int myHistoryNo) {
		this.myHistoryNo = myHistoryNo;
	}
	public int getResultNo() {
		return resultNo;
	}
	public void setResultNo(int resultNo) {
		this.resultNo = resultNo;
	}
	public int getVideoNo() {
		return videoNo;
	}
	public void setVideoNo(int videoNo) {
		this.videoNo = videoNo;
	}
	public int getMapJobNo() {
		return mapJobNo;
	}
	public void setMapJobNo(int mapJobNo) {
		this.mapJobNo = mapJobNo;
	}
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}

}
