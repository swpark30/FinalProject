package com.jobdongsan.project.model;

public class QuestionVO {
	private int questionNo;
	private String questionCon;
	private String questionChar;
	private int categoryNo;
	
	
	public int getQuestionNo() {
		return questionNo;
	}
	public void setQuestionNo(int questionNo) {
		this.questionNo = questionNo;
	}
	public String getQuestionCon() {
		return questionCon;
	}
	public void setQuestionCon(String questionCon) {
		this.questionCon = questionCon;
	}
	public String getQuestionChar() {
		return questionChar;
	}
	public void setQuestionChar(String questionChar) {
		this.questionChar = questionChar;
	}
	public int getCategoryNo() {
		return categoryNo;
	}
	public void setCategoryNo(int categoryNo) {
		this.categoryNo = categoryNo;
	}
	
	
}
